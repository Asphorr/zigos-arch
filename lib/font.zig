        'P', 'p' => .{ 0x1E, 0x11, 0x1E, 0x10, 0x10, 0x10, 0x00 },
        'Q', 'q' => .{ 0x0E, 0x11, 0x11, 0x15, 0x12, 0x0D, 0x00 },
        'R', 'r' => .{ 0x1E, 0x11, 0x1E, 0x14, 0x12, 0x11, 0x00 },
