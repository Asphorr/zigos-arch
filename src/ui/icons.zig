pub fn iconForTitle(title: []const u8) ?*const [16][16]u32 {
    if (title.len >= 8 and title[0] == 'T' and title[1] == 'e' and title[2] == 'r' and title[3] == 'm') return &terminal;
    if (title.len >= 4 and title[0] == 'p' and title[1] == 'a' and title[2] == 'i' and title[3] == 'n') return &paint;
    if (title.len >= 4 and title[0] == 'P' and title[1] == 'a' and title[2] == 'i' and title[3] == 'n') return &paint;
    if (title.len >= 4 and title[0] == 'c' and title[1] == 'a' and title[2] == 'l' and title[3] == 'c') return &calc;
    if (title.len >= 4 and title[0] == 'C' and title[1] == 'a' and title[2] == 'l' and title[3] == 'c') return &calc;
    if (title.len >= 4 and title[0] == 's' and title[1] == 'e' and title[2] == 't' and title[3] == 't') return &gear;
    if (title.len >= 4 and title[0] == 'S' and title[1] == 'e' and title[2] == 't' and title[3] == 't') return &gear;
    if (title.len >= 4 and title[0] == 'f' and title[1] == 'i' and title[2] == 'l' and title[3] == 'e') return &folder;
    if (title.len >= 4 and title[0] == 'F' and title[1] == 'i' and title[2] == 'l' and title[3] == 'e') return &folder;
    if (title.len >= 3 and title[0] == 's' and title[1] == 'y' and title[2] == 's') return &monitor;
    if (title.len >= 3 and title[0] == 'S' and title[1] == 'y' and title[2] == 's') return &monitor;
    if (title.len >= 4 and title[0] == 'e' and title[1] == 'd' and title[2] == 'i' and title[3] == 't') return &editor;
    if (title.len >= 4 and title[0] == 'E' and title[1] == 'd' and title[2] == 'i' and title[3] == 't') return &editor;
    if (title.len >= 4 and title[0] == 'd' and title[1] == 'o' and title[2] == 'o' and title[3] == 'm') return &doom;
    if (title.len >= 4 and title[0] == 'D' and title[1] == 'o' and title[2] == 'o' and title[3] == 'm') return &doom;
    if (title.len >= 3 and title[0] == 'G' and title[1] == 'U' and title[2] == 'I') return &paint;
    return null;
}
